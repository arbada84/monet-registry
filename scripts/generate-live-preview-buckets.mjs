#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const registryDir = path.join(root, "src", "components", "registry");
const pageDir = path.join(root, "src", "components", "pages");
const outputDir = path.join(root, "src", "generated", "live-preview-buckets");
const pageOutputDir = path.join(root, "src", "generated", "page-live-preview");
const routeOutputDir = path.join(root, "src", "app", "live-preview-render", "buckets");
const pageRouteOutputDir = path.join(root, "src", "app", "page-live-preview-render", "buckets");
const oldPageRouteOutputDir = path.join(root, "src", "app", "page-live-preview-render", "pages");
const bucketCount = Number(process.env.LIVE_PREVIEW_BUCKET_COUNT || "32");
const pageBucketCount = Number(process.env.PAGE_LIVE_PREVIEW_BUCKET_COUNT || "8");

function hashName(name) {
  let hash = 2166136261;
  for (let i = 0; i < name.length; i += 1) {
    hash ^= name.charCodeAt(i);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

function bucketNameFor(name) {
  return `bucket-${String(hashName(name) % bucketCount).padStart(2, "0")}`;
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function rmDir(dir) {
  fs.rmSync(dir, { recursive: true, force: true });
}

function assertSafeComponentName(name) {
  if (!/^[a-z0-9][a-z0-9-]*$/i.test(name)) {
    throw new Error(`Unsafe registry component name: ${name}`);
  }
}

if (!fs.existsSync(registryDir)) throw new Error(`Registry directory not found: ${registryDir}`);
if (!fs.existsSync(pageDir)) throw new Error(`Page directory not found: ${pageDir}`);

const componentNames = fs
  .readdirSync(registryDir, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)
  .sort((a, b) => a.localeCompare(b));
const pageNames = fs
  .readdirSync(pageDir, { withFileTypes: true })
  .filter((entry) => entry.isDirectory())
  .map((entry) => entry.name)
  .sort((a, b) => a.localeCompare(b));

for (const name of componentNames) assertSafeComponentName(name);
for (const name of pageNames) assertSafeComponentName(name);

const buckets = new Map();
for (const name of componentNames) {
  const bucket = bucketNameFor(name);
  const names = buckets.get(bucket) || [];
  names.push(name);
  buckets.set(bucket, names);
}

const pageBuckets = new Map();
for (const name of pageNames) {
  const bucket = `bucket-${String(hashName(name) % pageBucketCount).padStart(2, "0")}`;
  const names = pageBuckets.get(bucket) || [];
  names.push(name);
  pageBuckets.set(bucket, names);
}

rmDir(outputDir);
rmDir(pageOutputDir);
rmDir(routeOutputDir);
rmDir(pageRouteOutputDir);
rmDir(oldPageRouteOutputDir);
ensureDir(outputDir);
ensureDir(pageOutputDir);
ensureDir(routeOutputDir);
ensureDir(pageRouteOutputDir);

const header = `// Generated by scripts/generate-live-preview-buckets.mjs. Do not edit manually.\n`;

fs.writeFileSync(
  path.join(outputDir, "index.ts"),
  `${header}
export const LIVE_PREVIEW_BUCKET_COUNT = ${bucketCount} as const;

function hashRegistryPreviewName(name: string): number {
  let hash = 2166136261;
  for (let i = 0; i < name.length; i += 1) {
    hash ^= name.charCodeAt(i);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

export function getRegistryPreviewBucketName(name: string): string {
  return \`bucket-\${String(hashRegistryPreviewName(name) % LIVE_PREVIEW_BUCKET_COUNT).padStart(2, "0")}\`;
}
`
);

for (let i = 0; i < bucketCount; i += 1) {
  const bucket = `bucket-${String(i).padStart(2, "0")}`;
  const names = buckets.get(bucket) || [];
  const entries = names
    .map((name) => `  ${JSON.stringify(name)}: () => import("@/components/registry/${name}/index"),`)
    .join("\n");

  fs.writeFileSync(
    path.join(outputDir, `${bucket}.ts`),
    `${header}
const registryComponentLoaders = {
${entries}
} as const;

export function getRegistryComponentLoader(name: string) {
  return registryComponentLoaders[name as keyof typeof registryComponentLoaders];
}
`
  );

  const routeDir = path.join(routeOutputDir, bucket, "[name]");
  ensureDir(routeDir);
  fs.writeFileSync(
    path.join(routeDir, "page.tsx"),
    `${header}
import { renderRegistryBucketPreview } from "@/app/live-preview-render/RegistryBucketPreviewPage";
import { getRegistryComponentLoader } from "@/generated/live-preview-buckets/${bucket}";

export const dynamic = "force-dynamic";

interface PageProps {
  params: Promise<{ name: string }>;
}

export default function RegistryBucket${String(i).padStart(2, "0")}PreviewPage({ params }: PageProps) {
  return renderRegistryBucketPreview({
    params,
    bucket: "${bucket}",
    getRegistryComponentLoader,
  });
}
`
  );
}

const counts = [...buckets.entries()]
  .map(([bucket, names]) => ({ bucket, count: names.length }))
  .sort((a, b) => a.bucket.localeCompare(b.bucket));

fs.writeFileSync(
  path.join(outputDir, "manifest.json"),
  `${JSON.stringify({ bucketCount, totalComponents: componentNames.length, buckets: counts }, null, 2)}\n`
);

fs.writeFileSync(
  path.join(pageOutputDir, "index.ts"),
  `${header}
export const PAGE_LIVE_PREVIEW_BUCKET_COUNT = ${pageBucketCount} as const;
export const PAGE_LIVE_PREVIEW_NAMES = ${JSON.stringify(pageNames, null, 2)} as const;

const pageLivePreviewNameSet = new Set<string>(PAGE_LIVE_PREVIEW_NAMES);

function hashPagePreviewName(name: string): number {
  let hash = 2166136261;
  for (let i = 0; i < name.length; i += 1) {
    hash ^= name.charCodeAt(i);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

export function isPageLivePreviewName(name: string): boolean {
  return pageLivePreviewNameSet.has(name);
}

export function getPagePreviewBucketName(name: string): string {
  return \`bucket-\${String(hashPagePreviewName(name) % PAGE_LIVE_PREVIEW_BUCKET_COUNT).padStart(2, "0")}\`;
}
`
);

const pageCounts = [...pageBuckets.entries()]
  .map(([bucket, names]) => ({ bucket, count: names.length }))
  .sort((a, b) => a.bucket.localeCompare(b.bucket));

fs.writeFileSync(
  path.join(pageOutputDir, "manifest.json"),
  `${JSON.stringify({ bucketCount: pageBucketCount, totalPages: pageNames.length, buckets: pageCounts, pages: pageNames }, null, 2)}\n`
);

for (let i = 0; i < pageBucketCount; i += 1) {
  const bucket = `bucket-${String(i).padStart(2, "0")}`;
  const names = pageBuckets.get(bucket) || [];
  const entries = names
    .map((name) => `  ${JSON.stringify(name)}: () => import("@/components/pages/${name}"),`)
    .join("\n");

  fs.writeFileSync(
    path.join(pageOutputDir, `${bucket}.ts`),
    `${header}
const pageComponentLoaders = {
${entries}
} as const;

export function getPageComponentLoader(name: string) {
  return pageComponentLoaders[name as keyof typeof pageComponentLoaders];
}
`
  );

  const routeDir = path.join(pageRouteOutputDir, bucket, "[name]");
  ensureDir(routeDir);
  fs.writeFileSync(
    path.join(routeDir, "page.tsx"),
    `${header}
import { renderPageBucketPreview } from "@/app/page-live-preview-render/PageBucketPreviewPage";
import { getPageComponentLoader } from "@/generated/page-live-preview/${bucket}";

export const dynamic = "force-dynamic";

interface PageProps {
  params: Promise<{ name: string }>;
}

export default function PageBucket${String(i).padStart(2, "0")}PreviewPage({ params }: PageProps) {
  return renderPageBucketPreview({
    params,
    bucket: "${bucket}",
    getPageComponentLoader,
  });
}
`
  );
}

const largestBucket = counts.reduce((largest, current) => (current.count > largest.count ? current : largest), {
  bucket: "",
  count: 0,
});
const largestPageBucket = pageCounts.reduce((largest, current) => (current.count > largest.count ? current : largest), {
  bucket: "",
  count: 0,
});

console.log(
  JSON.stringify(
    {
      generated: outputDir,
      pageGenerated: pageOutputDir,
      bucketCount,
      totalComponents: componentNames.length,
      totalPages: pageNames.length,
      largestBucket,
      pageBucketCount,
      largestPageBucket,
    },
    null,
    2
  )
);
